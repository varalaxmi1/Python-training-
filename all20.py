a = 10
b = 5
c = 20.0
d = 3.5
print(f"Initial values: a = {a}, b = {b}, c = {c}, d = {d}")
a += 3
print(f"After addition assignment (a += 3): a = {a}")
b -= 2
print(f"After subtraction assignment (b -= 2): b = {b}")
c *= 2
print(f"After multiplication assignment (c *= 2): c = {c}")
d /= 2
print(f"After division assignment (d /= 2): d = {d}")
a %= 5
print(f"After modulus assignment (a %= 5): a = {a}")
c **= 2
print(f"After exponentiation assignment (c **= 2): c = {c}")
d //= 1
print(f"After floor division assignment (d //= 1): d = {d}")
