a = 12  
b = 7   
bitwise_and = a & b
print(f"Bitwise AND of {a} and {b} is {bitwise_and}")  
bitwise_or = a | b
print(f"Bitwise OR of {a} and {b} is {bitwise_or}")   
bitwise_xor = a ^ b
print(f"Bitwise XOR of {a} and {b} is {bitwise_xor}")  
bitwise_not_a = ~a
print(f"Bitwise NOT of {a} is {bitwise_not_a}") 
left_shift = a << 2
print(f"Left shift {a} by 2 positions is {left_shift}") 
right_shift = a >> 2
print(f"Right shift {a} by 2 positions is {right_shift}") 
